// MARK: - LocalStorageModel
struct LocalStorageModel: Codable {
    let id: Int
    let readable: Bool
    let title, titleShort, titleVersion, isrc: String
    let link, share: String
    let duration, trackPosition, diskNumber, rank: Int
    let releaseDate: String
    let explicitLyrics: Bool
    let explicitContentLyrics, explicitContentCover: Int
    let preview: String
    let bpm: Int
    let gain: Double
    let availableCountries, contributors: [JSONAny]
    let md5Image: String
    let artist: LocalArtist
    let album: LocalAlbum
    let type: String

    enum CodingKeys: String, CodingKey {
        case id, readable, title
        case titleShort = "title_short"
        case titleVersion = "title_version"
        case isrc, link, share, duration
        case trackPosition = "track_position"
        case diskNumber = "disk_number"
        case rank
        case releaseDate = "release_date"
        case explicitLyrics = "explicit_lyrics"
        case explicitContentLyrics = "explicit_content_lyrics"
        case explicitContentCover = "explicit_content_cover"
        case preview, bpm, gain
        case availableCountries = "available_countries"
        case contributors
        case md5Image = "md5_image"
        case artist, album, type
    }
}

// MARK: - Album
struct LocalAlbum: Codable {
